export const generateUserName = (index: number): string => {
  const lastName = LAST_NAMES[index % LAST_NAMES.length];
  const firstName =
    FIRST_NAMES[Math.floor(index / LAST_NAMES.length) % FIRST_NAMES.length];
  return `${firstName} ${lastName}`;
};

export const generateUserEmail = (index: number): string => {
  const lastName = LAST_NAMES[index % LAST_NAMES.length];
  const firstName =
    FIRST_NAMES[Math.floor(index / LAST_NAMES.length) % FIRST_NAMES.length];
  const domain =
    EMAIL_DOMAINS[
      Math.floor(index / (FIRST_NAMES.length * LAST_NAMES.length)) %
        EMAIL_DOMAINS.length
    ];
  const nameCycle = Math.floor(
    index / (FIRST_NAMES.length * LAST_NAMES.length),
  );
  const localPart =
    nameCycle === 0
      ? `${firstName.toLowerCase()}.${lastName.toLowerCase()}`
      : `${firstName.toLowerCase()}.${lastName.toLowerCase()}${nameCycle + 1}`;
  return `${localPart}@${domain}`;
};
